#!/usr/bin/env node
/**
 * Embeds vendored Cursor Fetch rules (.mdc) + Innovation Lab markdown (v1.0.5) for the system prompt.
 *
 * PRODUCTION / END USERS: This script is NOT run when someone installs the VSIX. The extension
 * ships the pre-generated `fetchai_knowledge.generated.ts` (committed in git). "Fetching" Innovation
 * Lab content happens only here at maintainer build time — set INNOVATION_LAB_DOCS to a local clone
 * of versioned_docs/version-1.0.5, then commit the regenerated TS file so CI and users need zero
 * extra paths or network calls.
 *
 * Cursor rules: src/core/prompts/system-prompt/components/data/fetchai-cursor-rules.mdc (committed)
 * Innovation Lab: $INNOVATION_LAB_DOCS, or if unset defaults to
 *   $HOME/innovationlab-resources/innovation-labs/versioned_docs/version-1.0.5 (maintainer checkout)
 * Env: MAX_FETCHAI_KNOWLEDGE_LINES (default 2000) — Innovation Lab bundle length only
 *
 * Output: src/core/prompts/system-prompt/components/fetchai_knowledge.generated.ts
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const CURSOR_RULES_FILE = path.join(
	ROOT,
	"src/core/prompts/system-prompt/components/data/fetchai-cursor-rules.mdc",
);

const DEFAULT_DOCS = path.join(
	process.env.HOME || "",
	"innovationlab-resources/innovation-labs/versioned_docs/version-1.0.5",
);

const INPUT_DIR = process.env.INNOVATION_LAB_DOCS || DEFAULT_DOCS;
const MAX_LINES = Number.parseInt(
	process.env.MAX_FETCHAI_KNOWLEDGE_LINES || "2000",
	10,
);
const OUT_FILE = path.join(
	ROOT,
	"src/core/prompts/system-prompt/components/fetchai_knowledge.generated.ts",
);

function stripFrontmatter(content) {
	if (!content.startsWith("---")) {
		return content;
	}
	const rest = content.slice(3);
	const end = rest.indexOf("\n---");
	if (end === -1) {
		return content;
	}
	return rest.slice(end + 4).trimStart();
}

function collectFiles(dir) {
	/** @type {string[]} */
	const out = [];
	function walk(d) {
		if (!fs.existsSync(d)) {
			return;
		}
		for (const name of fs.readdirSync(d, { withFileTypes: true })) {
			const p = path.join(d, name.name);
			if (name.isDirectory()) {
				walk(p);
			} else if (/\.(md|mdx)$/.test(name.name)) {
				out.push(p);
			}
		}
	}
	walk(dir);
	return out.sort();
}

function buildInnovationLabBundle() {
	if (!fs.existsSync(INPUT_DIR)) {
		console.error(
			`[build-fetchai-knowledge] Missing docs directory: ${INPUT_DIR}`,
		);
		console.error(
			"Set INNOVATION_LAB_DOCS to your versioned_docs/version-1.0.5 path.",
		);
		process.exit(1);
	}

	const files = collectFiles(INPUT_DIR);
	/** @type {string[]} */
	const lines = [];
	lines.push(
		"--- BEGIN INNOVATION LAB BUNDLE (paths relative to docs root) ---",
		"",
	);

	for (const file of files) {
		const rel = path.relative(INPUT_DIR, file);
		let text = fs.readFileSync(file, "utf8");
		text = stripFrontmatter(text);
		const chunk = ["", `### FILE: ${rel}`, "", ...text.split(/\r?\n/)];
		for (const line of chunk) {
			lines.push(line);
			if (lines.length >= MAX_LINES) {
				break;
			}
		}
		if (lines.length >= MAX_LINES) {
			break;
		}
	}

	return {
		body: lines.slice(0, MAX_LINES).join("\n"),
		lineCount: lines.length,
	};
}

function main() {
	if (!fs.existsSync(CURSOR_RULES_FILE)) {
		console.error(
			`[build-fetchai-knowledge] Missing vendored Cursor rules: ${CURSOR_RULES_FILE}`,
		);
		process.exit(1);
	}

	const cursorRulesRaw = fs.readFileSync(CURSOR_RULES_FILE, "utf8");
	const cursorRulesBody = stripFrontmatter(cursorRulesRaw).trimEnd();

	const { body: labBody, lineCount: labLines } = buildInnovationLabBundle();

	const ts = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/build-fetchai-knowledge.mjs — do not edit by hand.
 *
 * FETCHAI_CURSOR_RULES_BODY: src/core/prompts/system-prompt/components/data/fetchai-cursor-rules.mdc (full file)
 * FETCHAI_KNOWLEDGE_BODY: Innovation Lab markdown version-1.0.5 (truncated to ${MAX_LINES} lines)
 *
 * Regenerate:
 *   INNOVATION_LAB_DOCS=/path/to/version-1.0.5 node scripts/build-fetchai-knowledge.mjs
 */
export const FETCHAI_CURSOR_RULES_BODY: string = ${JSON.stringify(cursorRulesBody)}

export const FETCHAI_KNOWLEDGE_BODY: string = ${JSON.stringify(labBody)}
`;

	fs.writeFileSync(OUT_FILE, ts, "utf8");
	console.log(
		`[build-fetchai-knowledge] Cursor rules: ${cursorRulesBody.split("\n").length} lines from ${path.relative(ROOT, CURSOR_RULES_FILE)}`,
	);
	console.log(
		`[build-fetchai-knowledge] Innovation Lab: ${labLines} lines -> ${path.relative(ROOT, OUT_FILE)}`,
	);
}

main();
